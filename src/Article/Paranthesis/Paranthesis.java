    public int getString(String s){
        char[] input = s.toCharArray();
        int count = 0;
        for (int i = 0; i < input.length; i++) {
            if(input[i] == '('){
                count++;
            }else if(input[i] == ')'){
                count--;
            }
        }
        return Math.abs(count);
    }
